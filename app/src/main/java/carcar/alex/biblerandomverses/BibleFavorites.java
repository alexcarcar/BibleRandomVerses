    public void save() {
        try {
            OutputStreamWriter out = new OutputStreamWriter(context.openFileOutput(BIBLE_FAVORITES, 0));
            for (Long favorite : this.favorites) {
                out.write(favorite + "\n");
            }
            out.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

